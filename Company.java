public class Company {
    public String name;
    public String email;
    public String town;
    public String occupation;
    public int foundingDate;
    public String founder;

    public Company(String name, String email, String town, String occupation, int foundingDate, String founder){
        this.name = name;
        this.email = email;
        this.town = town;
        this.occupation = occupation;
        this.foundingDate = foundingDate;
        this.founder = founder;
    }


}
